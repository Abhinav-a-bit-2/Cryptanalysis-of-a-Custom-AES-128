# 4. Encryption & Fault Injection
# ----------------------------------------------------------
def encrypt_with_fault(pt, key, n, inject_fault=False, fault_byte_idx=0, fault_val=0x01):
    state = list(pt)
    w = expand_key(key, n)
    
    # Initial Round
    state = add_round_key(state, w, 0)
    
    # Rounds 1 to 9
    for r in range(1, 10):
        state = sub_bytes(state, n)
        state = shift_rows(state)
        
        # --- FAULT INJECTION POINT ---
        # We inject the fault exactly before Round 8 MixColumns.
        # This creates the standard Piret DFA signature.
        if inject_fault and r == 8:
            state[fault_byte_idx] ^= fault_val
            
        state = mix_columns(state)
        state = add_round_key(state, w, r)
        
    # Final Round (Round 10 - No MixColumns)
    state = sub_bytes(state, n)
    state = shift_rows(state)
    state = add_round_key(state, w, 10)
    
    return bytes(state)

