# 2. AES Core Operations
# ----------------------------------------------------------
def sub_bytes(state, n):
    for i in range(16):
        # Uses the dynamic mapping 'n' to select the S-box for each byte
        state[i] = sbox_list[n[i]][state[i]]
    return state

def shift_rows(s):
    return [
        s[0],  s[5],  s[10], s[15],
        s[4],  s[9],  s[14], s[3],
        s[8],  s[13], s[2],  s[7],
        s[12], s[1],  s[6],  s[11]
    ]

def mix_columns(state):
    res = [0] * 16
    for c in range(4):
        v0, v1, v2, v3 = state[c*4], state[c*4+1], state[c*4+2], state[c*4+3]
        res[c*4]   = gf_mul(2, v0) ^ gf_mul(3, v1) ^ v2 ^ v3
        res[c*4+1] = v0 ^ gf_mul(2, v1) ^ gf_mul(3, v2) ^ v3
        res[c*4+2] = v0 ^ v1 ^ gf_mul(2, v2) ^ gf_mul(3, v3)
        res[c*4+3] = gf_mul(3, v0) ^ v1 ^ v2 ^ gf_mul(2, v3)
    return res

def add_round_key(state, w, round_num):
    for c in range(4):
        word = w[round_num * 4 + c]
        state[c*4]   ^= (word >> 24) & 0xFF
        state[c*4+1] ^= (word >> 16) & 0xFF
        state[c*4+2] ^= (word >> 8)  & 0xFF
        state[c*4+3] ^= word & 0xFF
    return state

