# 3. Dynamic Key Expansion
# ----------------------------------------------------------
def expand_key(key, n):
    w = [0] * 44
    for i in range(4):
        w[i] = (key[i*4]<<24) | (key[i*4+1]<<16) | (key[i*4+2]<<8) | key[i*4+3]
        
    for i in range(4, 44):
        temp = w[i-1]
        if i % 4 == 0:
            # RotWord
            temp = ((temp << 8) & 0xFFFFFFFF) | (temp >> 24)
            # SubWord using dynamic S-box mapping 'n'
            sub = (
                (sbox_list[n[(i*4 + 0) % 16]][(temp >> 24) & 0xFF] << 24) |
                (sbox_list[n[(i*4 + 1) % 16]][(temp >> 16) & 0xFF] << 16) |
                (sbox_list[n[(i*4 + 2) % 16]][(temp >> 8)  & 0xFF] << 8)  |
                (sbox_list[n[(i*4 + 3) % 16]][temp & 0xFF])
            )
            temp = sub ^ (RCON[i // 4] << 24)
            
        w[i] = w[i-4] ^ temp
    return w

